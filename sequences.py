# LISTS - sequence of mutable values / can be modified
# string
name = "Raphael"

# print out by index
print(f"{name[0]}, {name[1]}, {name[2]}, {name[3]}")

# multiple content in list
names = ["Jake", "Raph", "Raphael", "Ballague"]
print(names[0])

# TUPLE - opposite of lists
coordinateX = 10.0
coordinateY = 20.0

coordinate = (coordinateX, coordinateY)
print(coordinate)

